// Program to print the sine series

#include <stdio.h>
#include <math.h>
void main()
{
    printf("sin(0): %.2f\n", sin(0.0));
    printf("sin(30): %.2f\n", sin(30.0));
    printf("sin(45): %.2f\n", sin(45.0));
    printf("sin(60): %.2f\n", sin(60.0));
    printf("sin(90): %.2f\n", sin(90.0));
}