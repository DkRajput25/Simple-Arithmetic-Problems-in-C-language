// Program to print the cosine series

#include <stdio.h>
#include <math.h>
void main()
{
    printf("cos(0): %.2f\n", cos(0.0));
    printf("cos(30): %.2f\n", cos(30.0));
    printf("cos(45): %.2f\n", cos(45.0));
    printf("cos(60): %.2f\n", cos(60.0));
    printf("cos(90): %.2f\n", cos(90.0));
}